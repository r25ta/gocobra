package main

import (
	"fmt"
)

func main() {
	arrayint := [5]int64{1, 2, 3, 4, 5}

	fmt.Println("Array Int: ", arrayint)

	arraystring := [4]string{"Ronaldo", "Olivia", "Camila", "Diego"}

	fmt.Println("Array String: ", arraystring)

	arrayfloat := [5]float64{2.1, 3.4, 5.6, 2.6, 10.7}

	fmt.Println("Array Float: ", arrayfloat)
}
